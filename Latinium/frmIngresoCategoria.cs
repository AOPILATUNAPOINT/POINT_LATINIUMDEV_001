		private void frmIngresoCategoria_Load(object sender, System.EventArgs e)
		{
			miAcceso = new Acceso(cdsSeteoF, "111716");

			if (!Funcion.Permiso("1589", cdsSeteoF))
			{				
				MessageBox.Show("No puede ingresar al Reporte", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				BeginInvoke(new MethodInvoker(UnloadMe));
				return;		
			}
			if (miAcceso.BExportar) this.btnExcel.Enabled = true;

			#region Fecha
			this.dtDesde.CalendarInfo.MinDate = DateTime.Parse("01/01/2015");
			this.dtDesde.Value = DateTime.Today;

			this.dtDesde.CalendarInfo.MaxDate = DateTime.Today;			
			this.dtHasta.CalendarInfo.MaxDate = DateTime.Today;

			this.dtDesde.Value = "01/" + DateTime.Now.Month.ToString() + "/" + DateTime.Now.Year.ToString();			
			this.dtHasta.Value = DateTime.Today;

			#endregion Fecha

			#region Combos
			this.cmbBodega.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select Bodega, Nombre From Bodega Where Activo = 1 And Inventario = 1 Order By Bodega");
			this.cmbGrupo.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select idCategoria, Categoria From ArticuloCategoria Where Activo = 1 Order By Categoria");
			this.cmbDocumento.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select idTipoFactura, Nombre From CompraNumero Where idTipoFactura in (8, 9)");
			#endregion Combos
		}

		private void btnBuscar_Click(object sender, System.EventArgs e)
		{
			#region Validación
			if (!Validacion.vbComboVacio(this.cmbBodega, "Seleccione una Bodega")) return;
			if (!Validacion.vbComboVacio(this.cmbDocumento, "Seleccione una Documento")) return;

			if (this.dtDesde.Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione una fecha inicial", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				this.dtDesde.Focus();
				return;
			}

			if (this.dtHasta.Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione una fecha final", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				this.dtHasta.Focus();
				return;
			}
			#endregion Validación

			#region Inicia Valores
			int iBodega = 0;
			int iCategoria = 0;
			int iTipoFactura = 0;

			if (this.cmbBodega.ActiveRow != null) iBodega = (int)this.cmbBodega.Value;
			if (this.cmbGrupo.ActiveRow != null) iCategoria = (int)this.cmbGrupo.Value;
			if (this.cmbDocumento.ActiveRow != null) iTipoFactura = (int)this.cmbDocumento.Value;
	
			bool bFechas = false;

			DateTime dtDesde = DateTime.Today;
			DateTime dtHasta = DateTime.Today;

			if (this.dtDesde.Value != System.DBNull.Value || this.dtHasta.Value != System.DBNull.Value)
			{
				bFechas = true;

				dtDesde = (DateTime)this.dtDesde.Value;
				dtHasta = (DateTime)this.dtHasta.Value;
			}
			#endregion Inicia Valores

			string sSQL = string.Format("Exec ReporteBuscaIngresosBodega '{0}', {1}, {2}, {3}, {4}, '{5}', '{6}'", this.txtBusqueda.Text.ToString(), iBodega, iTipoFactura, iCategoria, bFechas, dtDesde.ToString("yyyyMMdd"), dtHasta.ToString("yyyyMMdd"));
			this.ultraGrid1.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);
			if (this.ultraGrid1.Rows.Count > 0) this.ultraGrid1.Focus(); else this.txtBusqueda.Focus();

			this.lblContador.Text = this.ultraGrid1.Rows.Count + " REGISTROS ENCONTRADOS";
		}

