		private void Aceptar()
		{
			if (this.ultraGrid1.ActiveRow == null)
			{
				MessageBox.Show("Seleccione una fila.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}

			if (this.ultraGrid1.ActiveRow.Cells["idRolPrestamo"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione una fila.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}
			
			int iEstado = Funcion.iEscalarSQL(cdsSeteoF, string.Format("Select * From RolPrestamo Where idRolPrestamo {0} And idRolTipoPrestamo = 13", (int)this.ultraGrid1.ActiveRow.Cells["idRolPrestamo"].Value));

			if (iEstado == 1)
			{
				MessageBox.Show("Ya se autorizó el pago no puede editar", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}

			if (iEstado == 2)
			{
				MessageBox.Show("Ya se realizo el pago no puede editar", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}

			this.DialogResult = DialogResult.OK;
		}

