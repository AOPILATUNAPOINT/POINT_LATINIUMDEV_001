		private void Aceptar()
		{
			if (this.ultraGrid1.Rows.Count == 0)
			{
				MessageBox.Show("No hay Prestamos encontrados", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtBusqueda.Focus();
				return;
			}

			if (this.ultraGrid1.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un Prestamo.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}
			
			if (this.ultraGrid1.ActiveRow.Cells["idPrestamo"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un Prestamo.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				return;
			}

			this.DialogResult = DialogResult.OK;
		}

