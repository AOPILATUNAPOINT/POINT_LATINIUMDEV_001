			ultraGrid1.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);		
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);	
		}

		private void btnContabilizar_Click(object sender, System.EventArgs e)
		{
			using (frmCuentasASF Cuentas = new frmCuentasASF((int)this.ultraGrid1.ActiveRow.Cells["idAnticipo"].Value, Convert.ToDecimal(this.ultraGrid1.ActiveRow.Cells["Saldo"].Value)))
			{
				if (DialogResult.OK == Cuentas.ShowDialog())
				{
					DateTime dtFecha = DateTime.Today;

					string sSQL = string.Format("CreaAsientoAnticiposSinFactura {0}, '{1}', '{2}', {3}, '{4}', '{5}'",
						(int)this.ultraGrid1.ActiveRow.Cells["idAsientoRS"].Value, dtFecha.ToString("yyyyMMdd"), this.ultraGrid1.ActiveRow.Cells["Secuencial"].Value.ToString(), 
						(int)this.ultraGrid1.ActiveRow.Cells["idProyecto"].Value, "");
					int idAsiento = Funcion.iEscalarSQL(cdsSeteoF, sSQL);

					Funcion.EjecutaSQL(cdsSeteoF, string.Format("Update Anticipos Set Saldo = 0, idAsientoRS = {0} Where idAnticipo = {1}", 
						(int)this.ultraGrid1.ActiveRow.Cells["idAsientoRS"].Value, (int)this.ultraGrid1.ActiveRow.Cells["idAnticipo"].Value));

					foreach(Infragistics.Win.UltraWinGrid.UltraGridRow dr in Cuentas.ultraGrid1.Rows.All)
					{
						if (dr.Cells["idCuenta"].Value != System.DBNull.Value) 
						{							
							sSQL = string.Format("Exec CreaAsientoDetalleAnticiposSinFactura {0}, {1}, {2}, {3}, '{4}'", 
								idAsiento, (int)dr.Cells["idCuenta"].Value, (int)this.ultraGrid1.ActiveRow.Cells["idProyecto"].Value, Convert.ToDecimal(dr.Cells["Valor"].Value), (int)this.ultraGrid1.ActiveRow.Cells["Descripcion"].Value);
							Funcion.EjecutaSQL(cdsSeteoF, sSQL, true);
						}
					}

					int idCuentaAP = Funcion.iEscalarSQL(cdsSeteoF, "Select idCuenta From AsignaCuenta Where idAsignacion = 19");
	
					sSQL = string.Format("Exec EgresoBancosCaja {0}, {1}, {2}, 0", 
						idAsiento, idCuentaAP, Convert.ToDecimal(this.ultraGrid1.ActiveRow.Cells["Saldo"].Value));
					Funcion.EjecutaSQL(cdsSeteoF, sSQL, true);
				}

			}
		}

