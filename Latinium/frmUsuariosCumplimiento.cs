		private void frmUsuariosCumplimiento_Load(object sender, System.EventArgs e)
		{
			this.ultraGrid1.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select idUsuario, idGrupo, Nombre, Recordatorios From Usuario Where Activo = 1 And idGrupo < 31 Order By idGrupo, Nombre");
		}

		private void ultraGrid1_CellChange(object sender, Infragistics.Win.UltraWinGrid.CellEventArgs e)
		{
			if (e.Cell.Column.ToString() == "Recordatorios")
			{
				this.ultraGrid1.UpdateData();
				Funcion.EjecutaSQL(cdsSeteoF, string.Format("Exec SeteoActualizaRecordatorioUsuario {0}, {1}", (int)e.Cell.Row.Cells["idUsuario"].Value, (bool)e.Cell.Row.Cells["Recordatorios"].Value));				
			}
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);		
		}

		private void frmUsuariosCumplimiento_KeyDown(object sender, System.Windows.Forms.KeyEventArgs e)
		{
			if (e.KeyCode == Keys.Escape) this.Close();
		}
