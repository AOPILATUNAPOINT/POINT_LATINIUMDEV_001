		}

		private void btnAceptar_Click(object sender, System.EventArgs e)
		{
			if (this.uGridResumen.ActiveRow.Cells["idProforma"].Value != System.DBNull.Value)
			{
				if ((int)this.uGridResumen.ActiveRow.Cells["idProforma"].Value > 0)
				{
					MessageBox.Show("Ya se genero la proforma del seguro para el registro seleccionado", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
					return;
				}
			}

			
			if (this.uGridResumen.ActiveRow.Cells["IEstado"].Value != System.DBNull.Value)
			{
				if ((int)this.uGridResumen.ActiveRow.Cells["idProforma"].Value == 5 )
				{
					MessageBox.Show("El cliente sellecionado tiene emitida una NDC ", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
					return;
				}
			}
				Aceptar();
		}
		private void Aceptar()
		{
			if (this.uGridResumen.Rows.Count == 0)
			{
				MessageBox.Show("No hay registros encontrados", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtRuc.Focus();			
			}
			else if (this.uGridResumen.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un registro", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else if (this.uGridResumen.ActiveRow.Cells["idCompra"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un registro", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else this.DialogResult = DialogResult.OK;
		}
