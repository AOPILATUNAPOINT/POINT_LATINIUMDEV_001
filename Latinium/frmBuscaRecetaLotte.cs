		private void btnBuscar_Click(object sender, System.EventArgs e)
		{
			string sSQL = string.Format("Exec BuscaRecetas '{0}'", this.txtBusqueda.Text.ToString());
			this.grdFacturas.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);
		}

		private void btnCancelar_Click(object sender, System.EventArgs e)
		{
			this.DialogResult = DialogResult.No;
		}

		private void btnAceptar_Click(object sender, System.EventArgs e)
		{
			if (this.grdFacturas.Rows.Count == 0)
			{
				MessageBox.Show("No hay documentos encontrados.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtBusqueda.Focus();			
			}
			else if (this.grdFacturas.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else if (this.grdFacturas.ActiveRow.Cells["idProducto"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else 
			{
				iProducto = (int)grdFacturas.ActiveRow.Cells["idProducto"].Value;
				iArticulo = (int)grdFacturas.ActiveRow.Cells["idArticulo"].Value;
				sArticulo = (string)grdFacturas.ActiveRow.Cells["Producto"].Value;
				sCodigo = (string)grdFacturas.ActiveRow.Cells["CodProducto"].Value;

				this.DialogResult = DialogResult.OK;
			}
		}

