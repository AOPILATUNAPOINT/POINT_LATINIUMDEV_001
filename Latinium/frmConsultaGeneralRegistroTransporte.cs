		private void btnBuscar_Click(object sender, System.EventArgs e)
		{
			#region Variables
			if (!Validacion.vbFechaEnDocumentos(this.dtDesde, "Seleccione la fecha inicial", false, "", cdsSeteoF)) return;
			if (!Validacion.vbFechaEnDocumentos(this.dtHasta, "Seleccione la fecha final", false, "", cdsSeteoF)) return;
			if (!Validacion.vbComparaFechas(Convert.ToDateTime(this.dtDesde.Value), Convert.ToDateTime(this.dtHasta.Value), this.dtDesde, "La fecha inicial no puede ser posterior a la fecha final", false)) return;
			
			#endregion Variables

			int iBodega = Funcion.iEscalarSQL(cdsSeteoF, string.Format("SELECT  idGrupo FROM dbo.USUARIO WHERE idUsuario = {0}", MenuLatinium.IdUsuario), true);
			if (iBodega == 23 || iBodega == 25)
			{
							if (!Validacion.vbComboVacio(this.cmbBodega, "Seleccione un local")) return;
			}
			int cmbBodega = 0;
			int cmbFormaPago = 0;
			if (this.cmbBodega.ActiveRow != null) cmbBodega = (int) this.cmbBodega.Value;
			if(this.cmbFormaPago.ActiveRow != null) cmbFormaPago = (int) this.cmbFormaPago.Value;
     
			#region Consulta
			this.lblContador.Text = "";
			this.uGridCreditos.DataSource = ultraDataSource1;

			string sSQL = string.Format("Exec [ConsultaGeneralRegistroTransporte] '{0}', '{1}', {2}, {3}, '{4}'", 
				Convert.ToDateTime(this.dtDesde.Value).ToString("yyyyMMdd"), Convert.ToDateTime(this.dtHasta.Value).ToString("yyyyMMdd"), 
				cmbBodega, cmbFormaPago, this.txtBusqueda.Text.ToString ());
			
			
			this.uGridCreditos.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);

			if (this.uGridCreditos.Rows.Count > 0) this.uGridCreditos.Focus();

			this.lblContador.Text = this.uGridCreditos.Rows.Count + " REGISTROS ENCONTRADOS";
			#endregion Consulta



		}

