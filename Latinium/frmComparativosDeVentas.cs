		private void btnVer_Click(object sender, System.EventArgs e)
		{
			string sLocales = "";

			foreach(Infragistics.Win.UltraWinGrid.UltraGridRow dr in this.uGridLocales.Rows.All)
			{
				if ((bool) dr.Cells["Sel"].Value)
				{
					if (sLocales.Length == 0) sLocales = dr.Cells["Bodega"].Value.ToString(); else if (sLocales.Length > 0) sLocales = sLocales + ", " + dr.Cells["Bodega"].Value.ToString();
				}
			}

			if (sLocales.Length == 0)
			{
				MessageBox.Show("Seleccione al menos un Local", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				return;
			}

			this.uGridComparativoGlobal.DataSource = Funcion.dvProcedimiento(cdsSeteoF, string.Format("Exec ReporteComparativoDeVentas 0, '{0}'", sLocales));
			this.uGridComparativoLocales.DataSource = Funcion.dvProcedimiento(cdsSeteoF, string.Format("Exec ReporteComparativoDeVentas 1, '{0}'", sLocales));

			//this.uGridComparativoLocales.DisplayLayout.Bands[0].SortedColumns.Add("Numero", false, true);
			this.uGridComparativoLocales.DisplayLayout.Bands[0].SortedColumns.Add("Mes", false, true);			
			this.uGridComparativoLocales.Rows.ExpandAll(true);
		}

		private void frmComparativosDeVentas_Load(object sender, System.EventArgs e)
		{
			this.uGridLocales.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select Bodega, Nombre, Convert(Bit, 1) As Sel From Bodega Where Activo = 1 And Factura = 1 Order By Nombre");
		}

