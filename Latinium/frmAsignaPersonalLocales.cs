		private void frmAsignaPersonalLocales_Load(object sender, System.EventArgs e)
		{
			miAcceso = new Acceso(cdsSeteoF, "0215");

			if (!Funcion.Permiso("928", cdsSeteoF))
			{				
				MessageBox.Show("No puede ingresar  ASIGNACION PERSONAL MATRIZ ", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				BeginInvoke(new MethodInvoker(UnloadMe));
				return;
			}
			
			this.Text = "";
			this.uGridInformacion.Visible = true;
			string sSQL = string.Format("Exec ConsultaNominaLocales 0");
			this.uGridInformacion.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);
			this.uGridInformacion.DataSource = this.ultraDataSource1;
			FuncionesProcedimientos.dsGeneral(sSQL, this.uGridInformacion);

			this.uGridInformacion.DisplayLayout.Bands[0].SortedColumns.Add("Descripcion", false, true);
			this.uGridInformacion.Rows.CollapseAll(true);
			 FuncionesProcedimientos.EstadoGrids(true, this.uGridInformacion);
	
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);	
		}

		private void uGridInformacion_CellChange(object sender, Infragistics.Win.UltraWinGrid.CellEventArgs e)
		{
			if (e.Cell.Row.Cells["idNominaAsignaLocales"].Value != System.DBNull.Value)
			{
				if (e.Cell.Column.ToString() == "Sel")
				{
					this.uGridInformacion.UpdateData();

					string sSQL = string.Format("Exec GuardaNominaAsignaLocales {0}, {1}, {2}, '{3}'", 
						(int)e.Cell.Row.Cells["idNominaAsignaLocales"].Value, 
						(int)e.Cell.Row.Cells["idDepartamento"].Value, 
						(int)e.Cell.Row.Cells["idPersonal"].Value, 
						e.Cell.Row.Cells["Codigo"].Value );
					Funcion.EjecutaSQL(cdsSeteoF, sSQL, true);
				}
			}
		}

