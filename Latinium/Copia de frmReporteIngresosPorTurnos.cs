		private void frmReporteIngresosPorTurnos_Load(object sender, System.EventArgs e)
		{
			this.dtDesde.CalendarInfo.MaxDate = DateTime.Today;

			this.dtHasta.CalendarInfo.MaxDate = DateTime.Today;
			

			this.dtDesde.Value = "01/" + DateTime.Now.Month.ToString() + "/" + DateTime.Now.Year.ToString();			
			this.dtHasta.Value = DateTime.Today;

			this.cmbBodega.DataSource = Funcion.dvProcedimiento(cdsSeteoF, string.Format("Exec ListaBodegas {0}, {1}", MenuLatinium.IdUsuario, 1));
		}

		private void btnExcel_Click(object sender, System.EventArgs e)
		{
			FuncionesProcedimientos.ExportaExcel(this.uGridReporte);
		}

		private void btnVer_Click(object sender, System.EventArgs e)
		{
			#region valida fechas
			if(!Validacion.vbFechaEnDocumentos(this.dtDesde,"Seleccione la Fecha Inicial",false,"",cdsSeteoF)) return;
			if(!Validacion.vbFechaEnDocumentos(this.dtHasta,"Seleccione la Fecha Final",false,"",cdsSeteoF)) return;
			if(!Validacion.vbComparaFechas((DateTime)this.dtDesde.Value, (DateTime)this.dtHasta.Value, this.dtHasta, "La fecha inicial no puede ser posterior a la fecha Final",false)) return;			
			#endregion valida fechas

			int idBodega= 0;
			if (this.cmbBodega.ActiveRow != null) idBodega = (int)this.cmbBodega.Value;

			string sSQL = string.Format("Exec ReporteIngresosPorTurnos '{0}', '{1}', {2}", 
				Convert.ToDateTime(this.dtDesde.Value).ToString("yyyyMMdd"), Convert.ToDateTime(this.dtHasta.Value).ToString("yyyyMMdd"), idBodega);
			FuncionesProcedimientos.dsGeneral(sSQL, this.uGridReporte);
		}

