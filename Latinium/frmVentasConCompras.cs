		{
			miAcceso = new Acceso(cdsSeteoF, "200412");

			if (!Funcion.Permiso("2007", cdsSeteoF))
			{			
				MessageBox.Show("No puede Ingresar a Reporte de Ventas con Compras", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				BeginInvoke(new MethodInvoker(UnloadMe));
				return;		
