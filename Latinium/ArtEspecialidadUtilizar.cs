		private void ArtEspecialidadUtilizar_Load(object sender, System.EventArgs e)
		{
			this.ultraGrid1.DataSource = Funcion.dvProcedimiento(cdsSeteoF, string.Format("Select idEspecialidad, Especialidad From EspecialidadUtilizar Order By Especialidad"));
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);	
		}

		private void btCancelar_Click(object sender, System.EventArgs e)
		{
			this.DialogResult = DialogResult.No;
		}

		private void btAceptar_Click(object sender, System.EventArgs e)
		{
			#region Guardar
			foreach(Infragistics.Win.UltraWinGrid.UltraGridRow drr in this.ultraGrid1.Rows.All)
			{
				if ((int)drr.Cells["idEspecialidad"].Value == 0)
				{
					string sSQLDetalle = string.Format("Exec [InsertaArticuloDentalis]  {0},'{1}',{2} ", 
						drr.Cells["idEspecialidad"].Value,
						drr.Cells["Especialidad"].Value.ToString(),5);     
						
					Funcion.EjecutaSQL(cdsSeteoF, sSQLDetalle);
				}
			}	
			#endregion Guardar
			this.DialogResult = DialogResult.OK;
		}

		private void ultraGrid1_AfterRowInsert(object sender, Infragistics.Win.UltraWinGrid.RowEventArgs e)
		{
			e.Row.Cells["idEspecialidad"].Value = 0;
	
		}

