		private void frmCre_ListaClientesCredito_Load(object sender, System.EventArgs e)
		{
			FuncionesProcedimientos.ListaBodegas(this.cmbBodega, MenuLatinium.IdUsuario, 1, DateTime.Today, false, cdsSeteoF);
		}

		private void btnVer_Click(object sender, System.EventArgs e)
		{
			int iBodega = 0;
			if (this.cmbBodega.ActiveRow != null) iBodega = (int)this.cmbBodega.Value;

			FuncionesProcedimientos.dsGeneral(string.Format("Exec Cre_ListaClientesCreditoRevisados {0}", iBodega), this.ultraGrid1);
		}

