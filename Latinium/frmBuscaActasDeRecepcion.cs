		private void btnBuscar_Click(object sender, System.EventArgs e)
		{			
			#region Estados
			int iEstado = 0;

			if (this.cmbCriterios.Text.ToString() == "TODOS") iEstado = 0;
			else if (this.cmbCriterios.Text.ToString() == "PENDIENTE") iEstado = 1;
			else if (this.cmbCriterios.Text.ToString() == "CANCELADO") iEstado = 2;
			else if (this.cmbCriterios.Text.ToString() == "ANULADO") iEstado = 3;
			#endregion Estados

			#region Variables
			bool bFechas = false;

			DateTime dtDesde = DateTime.Today;
			DateTime dtHasta = DateTime.Today;

			if (this.dtDesde.Value != System.DBNull.Value && this.dtHasta.Value != System.DBNull.Value)
			{
				bFechas = true;

				dtDesde = (DateTime)this.dtDesde.Value;
				dtHasta = (DateTime)this.dtHasta.Value;
			}

			if (this.cmbBodega.Visible) if (this.cmbBodega.ActiveRow != null) iBodega = (int)this.cmbBodega.Value;
			#endregion Variables
				
			#region Consulta
			this.uGridDetalle.DataSource = ultraDataSource1;

			string sSQL = string.Format("Exec ConsultaListaActaRecepcion '{0}', {1}, {2}, '{3}', '{4}', {5}", 
				this.txtBusqueda.Text.ToString(), iEstado, iBodega, dtDesde.ToString("yyyyMMdd"), dtHasta.ToString("yyyyMMdd"), bFechas);
			this.uGridDetalle.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);

			if (this.uGridDetalle.Rows.Count > 0) this.uGridDetalle.Focus();			
			#endregion Consulta
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);		
		}

		private void frmBuscaActasDeRecepcion_Load(object sender, System.EventArgs e)
		{
			if (iBodega > 1)
			{
				string sSQL = string.Format("Select Nombre From Bodega Where Bodega = {0}", iBodega);
				this.Text = "LOCAL : " + Funcion.sEscalarSQL(cdsSeteoF, sSQL).ToString().Trim();
			}
			else
			{								
				this.cmbBodega.DataSource = Funcion.dvProcedimiento(cdsSeteoF, "Select Codigo, Bodega, Nombre From Bodega Where Activo = 1 And Factura = 1 Order By Nombre");
				this.lblLocal.Visible = true;
				this.cmbBodega.Visible = true;				
			}
			
			this.cmbCriterios.Items.Add("TODOS");
			this.cmbCriterios.Items.Add("PENDIENTE");			
			this.cmbCriterios.Items.Add("CANCELADO");			
			this.cmbCriterios.Items.Add("ANULADO");
			this.cmbCriterios.Text = "TODOS";
		}

		private void Aceptar()
		{
			if (this.uGridDetalle.Rows.Count == 0)
			{
				MessageBox.Show("No hay documentos encontrados.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtBusqueda.Focus();			
			}
			else if (this.uGridDetalle.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else if (this.uGridDetalle.ActiveRow.Cells["idActaRecibido"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else this.DialogResult = DialogResult.OK;
		}

		private void btnAceptar_Click(object sender, System.EventArgs e)
		{
			Aceptar();
		}

