		private void btAceptar_Click(object sender, System.EventArgs e)
		{
			foreach(Infragistics.Win.UltraWinGrid.UltraGridRow dr in ultraGrid1.Rows.All)
			{
				if ((bool)this.ultraGrid1.ActiveRow.Cells["Sel"].Value)
				{
					string sSQL = string.Format("Exec CorrecionSerialesManuales {0}, {1}, {2}, {3}, '{4}', '{5}', {6}, '{7}', '{8}'", 
						(int)this.ultraGrid1.ActiveRow.Cells["idSerial"].Value, (int)this.ultraGrid1.ActiveRow.Cells["idDetalleSerial"].Value, 
						(int)this.ultraGrid1.ActiveRow.Cells["idDetCompra"].Value, (int)this.ultraGrid1.ActiveRow.Cells["idArticulo"].Value,
						this.ultraGrid1.ActiveRow.Cells["Manual"].Value.ToString(), this.ultraGrid1.ActiveRow.Cells["Serial"].Value.ToString(),
						(int)this.ultraGrid1.ActiveRow.Cells["Bodega"].Value, 
						this.ultraGrid1.ActiveRow.Cells["Observaciones"].Value.ToString(), this.ultraGrid1.ActiveRow.Cells["Numero"].Value.ToString());
				}
			}

			this.DialogResult = DialogResult.OK;
		}

