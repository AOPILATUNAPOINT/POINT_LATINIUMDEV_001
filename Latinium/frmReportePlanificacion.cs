		private void btnVer_Click(object sender, System.EventArgs e)
		{
			if (!Validacion.vbFechaEnDocumentos(this.dtDesde, "Seleccione la fecha inicial", false, "", cdsSeteoF)) return;
			if (!Validacion.vbFechaEnDocumentos(this.dtHasta, "Seleccione la fecha final", false, "", cdsSeteoF)) return;
			if (!Validacion.vbComparaFechas(Convert.ToDateTime(this.dtDesde.Value), Convert.ToDateTime(this.dtHasta.Value), this.dtDesde, "La fecha inicial no puede ser posterior a la fecha final", false)) return;

			string sSQLPa = string.Format("Exec ReporteGeoreferenciaEntrega '{0}', '{1}'", 
				Convert.ToDateTime(this.dtDesde.Value).ToString("yyyyMMdd"), Convert.ToDateTime(this.dtHasta.Value).ToString("yyyyMMdd") );
			FuncionesProcedimientos.dsGeneralModulo(sSQLPa, this.uGridResumen);

Hashtable conteo = new Hashtable();
			int totalDuplicados = 0;
			string duplicadosMensaje = "";

			foreach (Infragistics.Win.UltraWinGrid.UltraGridRow dr in this.uGridResumen.Rows.All)
			{
				if (dr.Cells["numero"].Value != null)
				{
					string numero = dr.Cells["numero"].Value.ToString().Trim();

					if (numero != "")
					{
						if (conteo.ContainsKey(numero))
						{
							int cantidad = (int)conteo[numero];
							conteo[numero] = cantidad + 1;
						}
						else
						{
							conteo[numero] = 1;
						}
					}
				}
			}

			foreach (DictionaryEntry item in conteo)
			{
				int cantidad = (int)item.Value;
				if (cantidad > 1)
				{
					totalDuplicados++;
					duplicadosMensaje += item.Key + " (repetido " + cantidad + " veces)\n";
				}
			}

			this.lblContador.Text = this.uGridResumen.Rows.Count + " REGISTROS ENCONTRADOS " + totalDuplicados + " DUPLICADOS";
		}

