		private void btnVer_Click(object sender, System.EventArgs e)
		{
			int Bodega = 0;
			bool bFechas = false;
			DateTime dtDesde = DateTime.Today;
			DateTime dtHasta = DateTime.Today;

			if (this.cmbBodega.ActiveRow != null) Bodega = (int)this.cmbBodega.Value;

			if (this.dtpDesde.Value != System.DBNull.Value && this.dtpHasta.Value != System.DBNull.Value)
			{
				dtDesde = Convert.ToDateTime(this.dtpDesde.Value);
				dtHasta = Convert.ToDateTime(this.dtpHasta.Value);

				bFechas = true;
			}

			string sSQL = string.Format("Exec RevisionPersonalCarpa {0}, {1}, {2}, '{3}', '{4}'", Bodega, (int)this.optEstados.Value, bFechas, dtDesde.ToString("yyyyMMdd"), dtHasta.ToString("yyyyMMdd"));
			this.uGridDetalle.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);

			this.lblContador.Text = this.uGridDetalle.Rows.Count + " Registros Encontrados";
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);
		}

		private void frmRevisionCarpa_Load(object sender, System.EventArgs e)
		{
			string sSQL = string.Format("Exec ListaBodegas {0}, 1", MenuLatinium.IdUsuario);
			this.cmbBodega.DataSource = Funcion.dvProcedimiento(cdsSeteoF, sSQL);

			sSQL = string.Format("Exec BodegaAsignadaPorUsuario {0}", MenuLatinium.IdUsuario);		
			int iBodega = Funcion.iEscalarSQL(cdsSeteoF, sSQL);

			if (iBodega > 0) this.cmbBodega.Value = iBodega; else this.cmbBodega.Enabled = true;
		}

		private void btnRevisado_Click(object sender, System.EventArgs e)
		{
			if ((int)this.optEstados.Value != 1)
			{
				MessageBox.Show("Seleccione en estado PENDIENTE para procesar", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				return;
			}

			foreach(Infragistics.Win.UltraWinGrid.UltraGridRow dr in this.uGridDetalle.Rows.All)
			{	
				string sSQL = string.Format("Exec ActualizaEstadoRevisadoPersonalCarpeo {0}", (int)dr.Cells["idPersonalCarpa"].Value);
				Funcion.EjecutaSQL(cdsSeteoF, sSQL);
			}
		}
