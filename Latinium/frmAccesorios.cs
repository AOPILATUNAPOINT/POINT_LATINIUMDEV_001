		private void frmAccesorios_KeyDown(object sender, System.Windows.Forms.KeyEventArgs e)
		{
			if (e.KeyCode == Keys.Escape) this.DialogResult = DialogResult.No;
		}

		private void btnAceptar_Click(object sender, System.EventArgs e)
		{
			int iSel = 0;

			foreach(Infragistics.Win.UltraWinGrid.UltraGridRow dr in this.ultraGrid1.Rows.All)
			{
				if ((bool)dr.Cells["Sel"].Value) iSel++;
			}

			if (iSel == 0)
			{
				MessageBox.Show("Seleccione por lo Menos un Accesorio", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
			}
			else this.DialogResult = DialogResult.OK;
		}

		private void frmAccesorios_Load(object sender, System.EventArgs e)
		{
			this.ultraGrid1.DataSource = Funcion.dvProcedimiento(cdsSeteoF, string.Format("Exec DTAccesoriosRetiro '{0}'", sFiltro));
		}

		private void cdsSeteoF_BeforeFill(object sender, C1.Data.FillEventArgs e)
		{
			Funcion miFuncion = new Funcion();
			cdsSeteoF.Schema.Connections[0].ConnectionString = miFuncion.AccesoBase(MenuLatinium.stDirFacturacion);	
		}

		private void ultraGrid1_AfterRowInsert(object sender, Infragistics.Win.UltraWinGrid.RowEventArgs e)
		{
			e.Row.Cells["idAccesorio"].Value = 0;				
			e.Row.Cells["Sel"].Value = false;
		}

		private void ultraGrid1_AfterCellUpdate(object sender, Infragistics.Win.UltraWinGrid.CellEventArgs e)
		{
			if (e.Cell.Column.ToString() == "Accesorio")
			{
				if (e.Cell.Row.Cells["Accesorio"].Value.ToString().Length > 0)
				{
					string sSQL = string.Format("Exec DTGrabaAccesorio {0}, '{1}'", (int)e.Cell.Row.Cells["idAccesorio"].Value, e.Cell.Row.Cells["Accesorio"].Value.ToString());
					e.Cell.Row.Cells["idAccesorio"].Value = Funcion.iEscalarSQL(cdsSeteoF, sSQL);
				}
			}
		}
