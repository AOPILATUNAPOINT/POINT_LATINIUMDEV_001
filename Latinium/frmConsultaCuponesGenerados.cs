		private void btnAceptar_Click(object sender, System.EventArgs e)
		{
			Aceptar();
		}

		private void Aceptar()
		{
			if (this.uGridCreditos.Rows.Count == 0)
			{
				MessageBox.Show("No hay clientes encontrados.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtBusqueda.Focus();			
			}
			else if (this.uGridCreditos.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un cliente.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else if (this.uGridCreditos.ActiveRow.Cells["idCuponesGenerados"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un cliente.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else 
			{				
				this.DialogResult = DialogResult.OK;
			}
		}

