		private void frmCre_ReporteRecepcionDocumentos_Load(object sender, System.EventArgs e)
		{
			miAcceso = new Acceso(cdsSeteoF, "081207");

			if (!Funcion.Permiso("933", cdsSeteoF))
			{				
				MessageBox.Show("No tiene Acceso RECEPCION DE DOCUMENTOS", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				BeginInvoke(new MethodInvoker(UnloadMe));
				return;
			}
			if(miAcceso.BExportar)this.btnExcel.Enabled = true;
			if(miAcceso.BVerDocumentos)this.btnVer.Enabled = true;

			DateTime dtFecha = DateTime.Parse("01/03/2016");
			this.dtDesde.CalendarInfo.MinDate = dtFecha.Date;

			this.dtDesde.Value = "01/" + DateTime.Now.Month.ToString() + "/" + DateTime.Now.Year.ToString();
			this.dtHasta.Value = DateTime.Today;			
		}

		private void btnExcel_Click(object sender, System.EventArgs e)
		{
			if (this.uGridCreditos.Rows.Count == 0)
			{
				MessageBox.Show("No hay Filas para Exportar", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Stop);
				return;
			}

			this.saveFileDialog1.Filter = "Microsoft Excel (*.xls)|*.xls";

			if(saveFileDialog1.ShowDialog() == System.Windows.Forms.DialogResult.OK && saveFileDialog1.FileName.Length > 0) 
			{				
				ultraGridExcelExporter1.Export(this.uGridCreditos, saveFileDialog1.FileName);
				System.Diagnostics.Process.Start(saveFileDialog1.FileName);
			}
		}

		private void btnVer_Click(object sender, System.EventArgs e)
		{
			if (this.uGridCreditos.Rows.Count == 0)
			{
				MessageBox.Show("No hay documentos encontrados.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);
				this.txtBuscar.Focus();			
			}
			else if (this.uGridCreditos.ActiveRow == null)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else if (this.uGridCreditos.ActiveRow.Cells["idCompra"].Value == System.DBNull.Value)
			{
				MessageBox.Show("Seleccione un documento.", "Point Technology", MessageBoxButtons.OK, MessageBoxIcon.Information);				
			}
			else 
			{
				frmCre_RecepcionDocumentos RDD = new frmCre_RecepcionDocumentos(1, this.uGridCreditos.ActiveRow.Cells["Ruc"].Value.ToString(), (DateTime)this.uGridCreditos.ActiveRow.Cells["Fecha"].Value);				
				RDD.Show();
			}
		}

