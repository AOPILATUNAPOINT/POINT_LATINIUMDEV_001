		private void ultraGrid1_KeyDown(object sender, System.Windows.Forms.KeyEventArgs e)
		{
			UltraGridState state = this.ultraGrid1.CurrentState;

			if ( ( state & UltraGridState.InEdit ) == 0 ) // No esta en edicion
			{
				switch (e.KeyCode)
				{
					case Keys.Enter:
						ultraGrid1.PerformAction(UltraGridAction.ExitEditMode, false, false);
						ultraGrid1.PerformAction(UltraGridAction.NextCellByTab, false, false);
						e.Handled = true;
						break;
					case Keys.F3:
						this.Cursor = Cursors.Hand;
//						Pago miPago = new Pago(1);
//						miPago.Show();
						this.Cursor = Cursors.Hand;
						break;
				}		
			}
			else // Cuando edita
			{
				switch (e.KeyCode)
				{
					case Keys.Enter:
						ultraGrid1.PerformAction(UltraGridAction.ExitEditMode, false, false);
						ultraGrid1.PerformAction(UltraGridAction.NextCellByTab, false, false);
						e.Handled = true;
						break;

					case Keys.F3:
						this.Cursor = Cursors.Hand;
//						Pago miPago = new Pago(1);
//						miPago.Show();
						this.Cursor = Cursors.Hand;
						break;
					case Keys.Up:
						ultraGrid1.PerformAction(UltraGridAction.ExitEditMode, false, false);
						ultraGrid1.PerformAction(UltraGridAction.AboveCell, false, false);
						e.Handled = true;
						ultraGrid1.PerformAction(UltraGridAction.EnterEditMode, false, false);
						break;
					case Keys.Down:
						if ((state & UltraGridState.IsDroppedDown) == 0)
						{
							ultraGrid1.PerformAction(UltraGridAction.ExitEditMode, false, false);
							ultraGrid1.PerformAction(UltraGridAction.BelowCell, false, false);
							e.Handled = true;
							ultraGrid1.PerformAction(UltraGridAction.EnterEditMode, false, false);
						}
						break;
					case Keys.F2:
						if ((state & UltraGridState.IsDroppedDown) == 0)
						{
//							ultraGrid1.PerformAction(UltraGridAction.ExitEditMode, false, false);
							ultraGrid1.PerformAction(UltraGridAction.ToggleDropdown, false, false);
							e.Handled = true;
//							ultraGrid1.PerformAction(UltraGridAction.EnterEditMode, false, false);
						}
						break;
				}		
			}
		}

